DROP TABLE IF EXISTS participation_requests;
DROP TABLE IF EXISTS compilation_events;
DROP TABLE IF EXISTS compilations;
DROP TABLE IF EXISTS events;
DROP TABLE IF EXISTS categories;
DROP TABLE IF EXISTS locations;
DROP TABLE IF EXISTS users;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id),
  CONSTRAINT UQ_USER_EMAIL UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS locations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  lat FLOAT NOT NULL,
  lon FLOAT NOT NULL,
  CONSTRAINT pk_location PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS categories (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(50) NOT NULL,
  CONSTRAINT pk_category PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS events (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  annotation TEXT NOT NULL,
  category_id BIGINT NOT NULL,
  created_on TIMESTAMP WITHOUT TIME ZONE,
  description TEXT NOT NULL,
  event_date TIMESTAMP WITHOUT TIME ZONE,
  initiator_id BIGINT NOT NULL,
  location_id BIGINT NOT NULL,
  paid BOOLEAN DEFAULT false,
  participant_limit INT DEFAULT 0,
  published_on TIMESTAMP WITHOUT TIME ZONE,
  request_moderation BOOLEAN DEFAULT false,
  state VARCHAR(100),
  title VARCHAR(255) NOT NULL,
  CONSTRAINT pk_event PRIMARY KEY (id),
  CONSTRAINT EVENT_CATEGORY_FK FOREIGN KEY (category_id) REFERENCES categories(id),
  CONSTRAINT EVENT_USER_FK FOREIGN KEY (initiator_id) REFERENCES users(id),
  CONSTRAINT EVENT_LOCATION_FK FOREIGN KEY (location_id) REFERENCES locations(id)
);

CREATE TABLE IF NOT EXISTS compilations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  pinned BOOLEAN DEFAULT false,
  title VARCHAR(50) NOT NULL,
  CONSTRAINT pk_compilation PRIMARY KEY (id)
);

CREATE TABLE IF NOT EXISTS compilation_events (
    event_id BIGINT NOT NULL,
    compilation_id BIGINT NOT NULL,
    CONSTRAINT EVENT_FK FOREIGN KEY (event_id) REFERENCES events(id),
    CONSTRAINT COMPILATION_FK FOREIGN KEY (compilation_id) REFERENCES compilations(id)
);

CREATE TABLE IF NOT EXISTS participation_requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  event_id BIGINT NOT NULL,
  requester_id BIGINT NOT NULL,
  status VARCHAR(100),
  created  TIMESTAMP WITHOUT TIME ZONE,
  CONSTRAINT pk_participation_request PRIMARY KEY (id),
  CONSTRAINT REQUEST_EVENT_FK FOREIGN KEY (event_id) REFERENCES events(id),
  CONSTRAINT REQUEST_USER_FK FOREIGN KEY (requester_id) REFERENCES users(id)
);


